export function getDistrictsByDivision(divisionName: string): District[] {
  const div = DIVISIONS.find(d => d.name === divisionName);
  if (!div) return [];
  return DISTRICTS.filter(d => d.divisionId === div.id).sort((a, b) => a.name.localeCompare(b.name));
}

export function getUpazilasByDistrict(districtName: string): Upazila[] {
  const dist = DISTRICTS.find(d => d.name === districtName);
  if (!dist) return [];
  return UPAZILAS.filter(u => u.districtId === dist.id).sort((a, b) => a.name.localeCompare(b.name));
}
